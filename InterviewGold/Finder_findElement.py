class Finder:
    def b_search(self, num_list, m, x, row):
        left = 0
        right = m-1
        while left <= right:
            mid = (left + right)//2
            if num_list[mid] == x:
                return [row, mid]
            elif x > num_list[mid]:
                left = mid +1
            else:
                right = mid -1
    #两次二分查找，第一次取第一列，定位出行，方法：mat[0,j]=x,直接返回0,j.否则ｗhile循环完毕，取得mid和mat[0,mid]
    #比较[0,mid],[0,mid-1],[0,mid+1]和ｘ的大小关系，定位出行
    #取出行，再次用二分查找
    def findElement(self, mat, n, m, x):
        left = 0
        right = n-1    #行总数             一定要注意这里的right的值，不然会出现下标越界错误
        while left <= right:
            mid = (left+right)//2
            if mat[mid][0] == x:
                return [mid, 0]
            elif x>mat[mid][0]:
                left = mid +1
            else:
                right = mid -1
        #依然没有找到

        if x < mat[mid][0]:
            if mid-1>=0 and x > mat[mid-1][0]:
                #去mid-1行找元素
                return self.b_search(mat[mid-1], m, x, mid-1)
            return None
        elif x > mat[mid][0]:
            return self.b_search(mat[mid], m, x, mid)

